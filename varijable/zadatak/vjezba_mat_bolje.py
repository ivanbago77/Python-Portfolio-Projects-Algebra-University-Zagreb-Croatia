prvi_broj = float(input("Dragi korisnice, molim te unesi prvi broj> "))
drugi_broj = float(input("Dragi korisnice, molim te unesi drugi broj> "))

zbroj = prvi_broj + drugi_broj
print(prvi_broj, "+", drugi_broj, "=", zbroj)

razlika = prvi_broj - drugi_broj
print(prvi_broj, "-", drugi_broj, "=", razlika)

umnozak = prvi_broj * drugi_broj
print(prvi_broj, "*", drugi_broj, "=", umnozak)

dijeljenje = prvi_broj / drugi_broj
print(prvi_broj, "/", drugi_broj, "=", dijeljenje)

potenciranje = prvi_broj**drugi_broj
print(prvi_broj, "^", drugi_broj, "=", potenciranje)

ostatak_dijeljenja = prvi_broj % drugi_broj
print(prvi_broj, "%", drugi_broj, "=", ostatak_dijeljenja)
